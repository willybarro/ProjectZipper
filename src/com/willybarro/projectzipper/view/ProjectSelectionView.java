    /** Creates new form ProjectSelectionView */
    public ProjectSelectionView(Project[] projects)
            throws Exception
    {
        if(projects.length == 0) {
            throw new Exception("There are no projects opened.");
        }
        
        // Creates listmodel with informed projects
        projectSelectionModel = new ProjectSelectionModel();
        for(Project project : projects) {
            projectSelectionModel.addElement(project);
        }

        initComponents();
        
        // Sets the cell render which will make the main project selected and bold
        getListProjects().setCellRenderer(new ProjectSelectionCellRenderer());
    }
    

