    public Project getListSelectedProject() {
        ProjectSelectionModel model = (ProjectSelectionModel) getListProjects().getModel();
        int selectedIndex = getListProjects().getSelectedIndex();
        if(selectedIndex > -1) {
            return model.get(selectedIndex);
        }
        return null;
    }

